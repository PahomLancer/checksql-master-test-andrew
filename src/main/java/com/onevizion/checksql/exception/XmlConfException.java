package com.onevizion.checksql.exception;

public class XmlConfException extends OnevizionException {

    public XmlConfException() {
        super();
    }

    public XmlConfException(String message, Throwable cause) {
        super(message, cause);
    }

    public XmlConfException(String message) {
        super(message);
    }

    public XmlConfException(Throwable cause) {
        super(cause);
    }

    public XmlConfException(String message, Throwable cause, Object ... params) {
        super(message, cause, params);
    }

    public XmlConfException(String message, Object ... params) {
        super(message, params);
    }

}